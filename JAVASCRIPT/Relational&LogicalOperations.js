//RELATIONAL OPERATIONS(<,> etc)

//comparing 2 integers
let x = 6;
let y =4;
let num3= x > y;
console.log(num3);

//comparing two strings
let x1="Home";
let y1="House";
let num4= x1 > y1;
console.log(num4);

//comparing different data types
let x3="4";
let y3=2;
let num5 = x3 > y3; //(IT WILL AUTOMETICALLY CONVERT THE DATA TYPE AND THEN COMPARE)
console.log(num5);

//equal signs(ALWAYS USE 3 === equal signs)

let x4 =5;
let y4=5;
let num6= x4 === y4;
console.log(num6);

//LOGICAL OPERATORS( && . ||)
let data1 = x > y && x4 === y4;
console.log(data1);

